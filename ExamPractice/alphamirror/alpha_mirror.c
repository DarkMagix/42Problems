#include <unistd.h>

void ft_putchar(char c)
{
	write(1, &c, 1);
}

int main(void)
{
	char c;

	c = 'a';
	while (c <= 'z')
	{
		ft_putchar('z' - (c - 'a'));
		c++;
	}
	return (0);
}
